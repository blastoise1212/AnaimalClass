package animalclass;

/**
 *
 * @author mkreiner
 */
public abstract class Bird extends Animal{
    private double wingSpan;

    public Bird(double wingSpan, String name) {
        super(name);
        this.wingSpan = wingSpan;
    }

    public double getWingSpan() {
        return wingSpan;
    }

    public void setWingSpan(double wingSpan) {
        this.wingSpan = wingSpan;
    }
}
